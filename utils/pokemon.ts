// Updated function to get random Pokemon from specific gen or all gens
export const getRandomPokemonName = (generation?: 1 | 2 | 3): string => {
    if (generation) {
        const genPokemon = {
            1: GEN1_POKEMON,
            2: GEN2_POKEMON,
            3: GEN3_POKEMON,
        }[generation];
        return genPokemon[Math.floor(Math.random() * genPokemon.length)];
    }
    return ALL_POKEMON[Math.floor(Math.random() * ALL_POKEMON.length)];
};

